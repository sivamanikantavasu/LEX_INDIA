-- 1. Profiles: Users can view all profiles, but only update their own.
CREATE POLICY "Public profiles are viewable by everyone." ON profiles FOR SELECT USING (true);
CREATE POLICY "Users can insert their own profile." ON profiles FOR INSERT WITH CHECK (auth.uid() = id);
CREATE POLICY "Users can update own profile." ON profiles FOR UPDATE USING (auth.uid() = id);

-- 2. Articles: Everyone can view 'published', only authors/admins can manage.
CREATE POLICY "Anyone can view published articles." ON articles FOR SELECT USING (status = 'published');
CREATE POLICY "Authors can view their own drafts." ON articles FOR SELECT USING (auth.uid() = author_id);
CREATE POLICY "Authors can insert their own articles." ON articles FOR INSERT WITH CHECK (auth.uid() = author_id);
CREATE POLICY "Authors can update their own articles." ON articles FOR UPDATE USING (auth.uid() = author_id);

-- 3. Categories: Anyone can view, only admin can manage (Admin role checks omitted for simplicity, but can be added).
CREATE POLICY "Anyone can view categories." ON categories FOR SELECT USING (true);

-- 4. Quizzes: Anyone can view.
CREATE POLICY "Anyone can view quizzes." ON quizzes FOR SELECT USING (true);
CREATE POLICY "Anyone can view questions." ON questions FOR SELECT USING (true);

-- 5. Quiz Results: Users can view their own results.
CREATE POLICY "Users can view their own results." ON quiz_results FOR SELECT USING (auth.uid() = user_id);
CREATE POLICY "Users can insert their own results." ON quiz_results FOR INSERT WITH CHECK (auth.uid() = user_id);

-- 6. Bookmarks: Users can manage their own bookmarks.
CREATE POLICY "Users can view their own bookmarks." ON bookmarks FOR SELECT USING (auth.uid() = user_id);
CREATE POLICY "Users can insert their own bookmarks." ON bookmarks FOR INSERT WITH CHECK (auth.uid() = user_id);
CREATE POLICY "Users can delete their own bookmarks." ON bookmarks FOR DELETE USING (auth.uid() = user_id);

-- 7. Forum: Anyone can view threads/posts, users can create and manage their own.
CREATE POLICY "Anyone can view forum." ON forum_threads FOR SELECT USING (true);
CREATE POLICY "Anyone can view posts." ON forum_posts FOR SELECT USING (true);
CREATE POLICY "Users can create threads." ON forum_threads FOR INSERT WITH CHECK (auth.uid() = author_id);
CREATE POLICY "Users can create posts." ON forum_posts FOR INSERT WITH CHECK (auth.uid() = author_id);

-- 8. Advisory: Citizens can view their own, experts can view assigned.
CREATE POLICY "Citizens can view their own requests." ON advisory_requests FOR SELECT USING (auth.uid() = citizen_id);
CREATE POLICY "Experts can view assigned requests." ON advisory_requests FOR SELECT USING (auth.uid() = expert_id);
CREATE POLICY "Citizens can create requests." ON advisory_requests FOR INSERT WITH CHECK (auth.uid() = citizen_id);
CREATE POLICY "Experts can update assigned requests." ON advisory_requests FOR UPDATE USING (auth.uid() = expert_id);

-- 9. Comments: Anyone can view, users can manage their own.
CREATE POLICY "Anyone can view comments." ON comments FOR SELECT USING (true);
CREATE POLICY "Users can insert their own comments." ON comments FOR INSERT WITH CHECK (auth.uid() = user_id);
CREATE POLICY "Users can update their own comments." ON comments FOR UPDATE USING (auth.uid() = user_id);
CREATE POLICY "Users can delete their own comments." ON comments FOR DELETE USING (auth.uid() = user_id);

-- 10. Likes: Anyone can view, users can manage their own.
CREATE POLICY "Anyone can view likes." ON likes FOR SELECT USING (true);
CREATE POLICY "Users can insert their own likes." ON likes FOR INSERT WITH CHECK (auth.uid() = user_id);
CREATE POLICY "Users can delete their own likes." ON likes FOR DELETE USING (auth.uid() = user_id);

-- 11. Notifications: Users can view and update their own.
CREATE POLICY "Users can view their own notifications." ON notifications FOR SELECT USING (auth.uid() = user_id);
CREATE POLICY "Users can update their own notifications." ON notifications FOR UPDATE USING (auth.uid() = user_id);

-- 12. Achievements: Anyone can view, system manages inserts.
CREATE POLICY "Anyone can view achievements." ON achievements FOR SELECT USING (true);
CREATE POLICY "Users can view their earned achievements." ON user_achievements FOR SELECT USING (true);

-- Trigger for profile creation on signup
-- Note: This is a common pattern for Supabase to automatically create a profile record when a user signs up.
CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS trigger AS $$
BEGIN
  INSERT INTO public.profiles (id, username, full_name, avatar_url, role)
  VALUES (new.id, new.raw_user_meta_data->>'username', new.raw_user_meta_data->>'full_name', new.raw_user_meta_data->>'avatar_url', (new.raw_user_meta_data->>'role')::user_role);
  RETURN new;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

